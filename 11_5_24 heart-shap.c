

#include <stdio.h>

int main()
{
     int i, j, k;
     for(i=1;i<=3;i++)
     {
         for(j=1;j<=17;j++)
         {
             if(j>=3-i && j<=6+i || j>=12-i && j<=15+i)
             {
                 printf("*");
             }
             else
             {
                 printf(" ");
             }
         }
         printf("\n");
     }
    for(i=9;i>=1;i--)
    {
        for(k=i;k<9;k++)
        {
            printf(" ");
        }
        for(j=1;j<=(2*i-1);j++)
        {
            printf("*");
        }
        printf("\n");
    }
    
    return 0;
}



